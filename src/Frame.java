    public String generateResponse(String userInput) {
        String response="I don't understand please be specific ?";
        
        userInput=userInput.toLowerCase();
        Boolean found=false;
        
         if (userInput.contains("hello") || userInput.contains("hi")) {
            response= "Hi "+name+"! How can I help you today?";
            found=true;
        }
        if(found==false)
        {
        
           for(String oid : orders.orderID){
               
               
               if(userInput.contains(oid)){
                     if(userInput.contains("ordername") || userInput.contains("name") || userInput.contains("what"))
                      {
                            response=" order name is : "+orders.orderName.get(oid);
                            found=true;
                      }else if(userInput.contains("payStatus") || userInput.contains("pay") || userInput.contains("payment"))
                      {
                            response=" order payment status is : "+orders.payStatus.get(oid);
                            found=true;
                      }else if(userInput.contains("price") || userInput.contains("total") || userInput.contains("value"))
                      {
                            response=" order price is : "+orders.price.get(oid);
                            found=true;
                      }else  if(userInput.contains("deliveryStatus") || userInput.contains("delivery") || userInput.contains("delivery"))
                      {
                            response=" order deliveryStatus is : "+orders.deliveryStatus.get(oid);
                            found=true;
                      } else  
                      {
                            response="details of orderid "+oid+"=>    order name is : "+orders.orderName.get(oid)+" order payment status is : "+orders.payStatus.get(oid)+" order price is : "+orders.price.get(oid)+" order deliveryStatus is  "+orders.deliveryStatus.get(oid);
                            found=true;
                      }
               
                   }
           
        }
        }
        if (found==false &&(userInput.contains("order") || userInput.contains("orders")))
        {    response=" id's of your orders is";
        System.out.println(" going inside orderssss");
            
              for(String str : orders.orderID){
                response += " "+str;
                
                
           }
              response=name+",you have currently ordered "+orders.orderID.size()+" item. "+response+" choose anyone for details ";
             found=true;
            
        }
        if(found==false)
        {
            Answer fromfile=new Answer();
            response=fromfile.getAnswer(userInput);
            found=true;
        }
        
        
        return response;
    }
